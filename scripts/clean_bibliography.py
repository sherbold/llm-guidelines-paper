def parse_args():
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--force", action="store_true",
                   help="Ignore cache; re-validate every entry.")
    p.add_argument("--only", default="",
                   help="Comma-separated list of citation keys to process exclusively.")
    p.add_argument("--max-age-days", type=int, default=30,
                   help="Re-validate cached entries older than N days (default 30).")
    p.add_argument("--remove-unref", action="store_true",
                   help="Run Step 1 (remove unreferenced entries). Default off.")
    p.add_argument("--no-rename", action="store_true",
                   help="Don't rename citation keys to DBLP keys.")
    p.add_argument("--no-tex-update", action="store_true",
                   help="Don't rewrite cite sites in .tex files (still rename in bib).")
    p.add_argument("--no-strip", action="store_true",
                   help="Don't strip arXiv metadata from already-published entries.")
    p.add_argument("--dry-run", action="store_true",
                   help="Print actions; don't write any files.")
    p.add_argument("--limit", type=int, default=0,
                   help="Process at most N entries (0 = no limit). Useful for testing.")
    return p.parse_args()


def select_entries(bib_db, only_keys):
    """Yield (idx, entry) for entries to consider, applying --only filter."""
    for i, e in enumerate(bib_db.entries):
        if only_keys and e["ID"] not in only_keys:
            continue
        yield i, e


def process_entry(entry, args):
    """Run lookup + decide outcome. Returns dict:
        {outcome, new_entry, new_key, stripped_fields, reason}
    Possible outcomes:
        upgraded      — replaced with non-CoRR DBLP record (rename candidate)
        normalized    — refreshed with DBLP record (same venue category)
        stripped      — only stripped arxiv metadata
        no_change     — nothing to do
        no_match      — no DBLP/Crossref result
        skip_misc     — @misc with howpublished URL
    """
    if is_misc_with_url(entry):
        return {"outcome": "skip_misc", "new_entry": None, "new_key": None,
                "stripped_fields": [], "reason": "@misc with howpublished URL"}

    new_entry, new_rec_key, source, reason = lookup_better_record(entry)

    if new_entry is None:
        # No DBLP match. Optionally still strip arxiv meta from published-looking
        # entries that have a manually-curated venue (rare).
        if not args.no_strip and is_published_with_arxiv_meta(entry):
            stripped = strip_arxiv_meta(entry)
            return {"outcome": "stripped", "new_entry": None, "new_key": None,
                    "stripped_fields": stripped, "reason": "kept entry; stripped arxiv meta"}
        return {"outcome": "no_match", "new_entry": None, "new_key": None,
                "stripped_fields": [], "reason": reason}

    # Guard: never downgrade a published local entry to a CoRR DBLP record.
    # If DBLP only has the preprint but the local entry already names a venue,
    # keep local and only strip arxiv meta.
    if is_arxiv_or_corr(new_entry) and not is_arxiv_or_corr(entry):
        if not args.no_strip and is_published_with_arxiv_meta(entry):
            stripped = strip_arxiv_meta(entry)
            return {"outcome": "stripped", "new_entry": None, "new_key": None,
                    "stripped_fields": stripped,
                    "reason": "DBLP only has CoRR; kept local venue, stripped arxiv meta"}
        return {"outcome": "no_change", "new_entry": None, "new_key": None,
                "stripped_fields": [],
                "reason": "DBLP only has CoRR; local already has venue"}

    # Compute the candidate citation key. We rename to a DBLP key whenever
    # one is available (uniformity across the bib), unless --no-rename is set.
    upgraded = is_arxiv_or_corr(entry) and not is_arxiv_or_corr(new_entry)
    candidate_id = entry["ID"]
    if not args.no_rename and new_rec_key:
        candidate_id = f"DBLP:{new_rec_key}"
    new_entry["ID"] = candidate_id

    # Strip arxiv meta from the new entry if it now has a venue
    if not args.no_strip and is_published_with_arxiv_meta(new_entry):
        strip_arxiv_meta(new_entry)

    if upgraded:
        return {"outcome": "upgraded", "new_entry": new_entry,
                "new_key": candidate_id if candidate_id != entry["ID"] else None,
                "stripped_fields": [], "reason": f"via {source}"}

    # Same category — replace if DBLP record adds metadata or rename is needed.
    metadata_fields = {"doi", "pages", "volume", "number", "publisher", "booktitle"}
    local_count = sum(1 for f in metadata_fields if entry.get(f))
    new_count = sum(1 for f in metadata_fields if new_entry.get(f))
    key_changes = candidate_id != entry["ID"]
    if new_count > local_count or key_changes:
        return {"outcome": "normalized", "new_entry": new_entry,
                "new_key": candidate_id if key_changes else None,
                "stripped_fields": [], "reason": f"via {source}"}

    # No improvement; check if we should at least strip arxiv meta from current
    if not args.no_strip and is_published_with_arxiv_meta(entry):
        stripped = strip_arxiv_meta(entry)
        return {"outcome": "stripped", "new_entry": None, "new_key": None,
                "stripped_fields": stripped, "reason": "DBLP not better; stripped arxiv meta"}

    return {"outcome": "no_change", "new_entry": None, "new_key": None,
            "stripped_fields": [], "reason": ""}


def write_report(removed, results, rename_map, tex_counts):
    lines = []
    lines.append("=" * 72)
    lines.append(f"Bibliography Cleanup Report  ({now_iso()})")
    lines.append("=" * 72)
    lines.append("")
    counts = {}
    for r in results.values():
        counts[r["outcome"]] = counts.get(r["outcome"], 0) + 1
    if removed:
        lines.append(f"Unreferenced entries removed:    {len(removed)}")
    for label in ("upgraded", "normalized", "stripped",
                  "no_change", "no_match", "skip_misc", "cached"):
        if label in counts:
            lines.append(f"{label:>32}:  {counts[label]}")
    lines.append("")

    if removed:
        lines.append("-" * 72)
        lines.append("Removed (unreferenced):")
        lines.append("-" * 72)
        for k in sorted(removed):
            lines.append(f"  - {k}")
        lines.append("")

    upgraded_keys = [k for k, r in results.items() if r["outcome"] == "upgraded"]
    if upgraded_keys:
        lines.append("-" * 72)
        lines.append("Upgraded to peer-reviewed venue:")
        lines.append("-" * 72)
        for k in sorted(upgraded_keys):
            r = results[k]
            new = rename_map.get(k, k)
            arrow = f"  {k}" if new == k else f"  {k} -> {new}"
            lines.append(f"{arrow}    [{r['reason']}]")
        lines.append("")

    normalized_keys = [k for k, r in results.items() if r["outcome"] == "normalized"]
    if normalized_keys:
        lines.append("-" * 72)
        lines.append("Normalized (refreshed entry, same category):")
        lines.append("-" * 72)
        for k in sorted(normalized_keys):
            lines.append(f"  - {k}    [{results[k]['reason']}]")
        lines.append("")

    stripped_entries = [(k, r["stripped_fields"]) for k, r in results.items()
                        if r["outcome"] == "stripped"]
    if stripped_entries:
        lines.append("-" * 72)
        lines.append("Stripped arXiv metadata from already-published entries:")
        lines.append("-" * 72)
        for k, fields in sorted(stripped_entries):
            lines.append(f"  - {k}    [stripped: {', '.join(fields)}]")
        lines.append("")

    no_match = [(k, r["reason"]) for k, r in results.items() if r["outcome"] == "no_match"]
    if no_match:
        lines.append("-" * 72)
        lines.append("No DBLP/Crossref match (review manually):")
        lines.append("-" * 72)
        for k, reason in sorted(no_match):
            lines.append(f"  - {k}    [{reason}]")
        lines.append("")

    if tex_counts:
        lines.append("-" * 72)
        lines.append("Cite-site rewrites in .tex files:")
        lines.append("-" * 72)
        for path, n in sorted(tex_counts.items()):
            lines.append(f"  {n:>4}  {path}")
        lines.append("")

    REPORT_FILE.write_text("\n".join(lines))
    print(f"Report written to {REPORT_FILE.name}")


def main():
    args = parse_args()
    only_keys = set(k.strip() for k in args.only.split(",") if k.strip())

    # Backup — only create if none exists so that a re-run never clobbers the
    # original. (Git history is the authoritative source; the backup is just
    # belt-and-braces for the first run before any external commit.)
    if not args.dry_run:
        if not BACKUP_FILE.exists():
            shutil.copy2(BIB_FILE, BACKUP_FILE)
            print(f"Backup: {BACKUP_FILE.name} (created)")
        else:
            print(f"Backup: {BACKUP_FILE.name} (kept existing — not overwritten)")

    # Parse bib
    parser = BibTexParser(common_strings=True)
    parser.ignore_nonstandard_types = False
    with open(BIB_FILE, encoding="utf-8") as f:
        bib_db = bibtexparser.load(f, parser=parser)
    print(f"Parsed {len(bib_db.entries)} entries")

    # Step 1: optional remove-unreferenced
    removed = []
    if args.remove_unref:
        print("\n--- Step 1: Remove unreferenced entries ---")
        referenced = keys_from_tex_files() | keys_from_aux()
        print(f"  Referenced (.tex ∪ .aux): {len(referenced)}")
        removed = remove_unreferenced(bib_db, referenced)
        print(f"  Removed: {len(removed)}")

    # Step 2/3/4: validate each entry
    print("\n--- Validate entries against DBLP / Crossref ---")
    cache = load_cache()
    results = {}        # citation_key -> result dict
    rename_map = {}     # old_key -> new_key
    new_cache = dict(cache)

    selectable = list(select_entries(bib_db, only_keys))
    if args.limit:
        selectable = selectable[:args.limit]
    total = len(selectable)
    processed = 0

    for n, (i, entry) in enumerate(selectable, 1):
        key = entry["ID"]
        chash = entry_hash(entry)

        if not args.force and is_fresh(cache.get(key), chash, args.max_age_days):
            results[key] = {"outcome": "cached", "reason": "cache fresh",
                            "new_entry": None, "new_key": None,
                            "stripped_fields": []}
            continue

        processed += 1
        print(f"  [{n}/{total}] {key[:55]:<55}", end=" ", flush=True)
        try:
            r = process_entry(entry, args)
        except Exception as e:
            print(f"ERROR: {e}")
            r = {"outcome": "no_match", "new_entry": None, "new_key": None,
                 "stripped_fields": [], "reason": f"exception: {e}"}
        results[key] = r

        # Apply changes to bib_db
        new_key_for_cache = key
        if r["outcome"] in ("upgraded", "normalized") and r["new_entry"] is not None:
            new_entry = r["new_entry"]
            new_id = new_entry["ID"]
            bib_db.entries[i] = new_entry
            label = r["outcome"].upper()
            if new_id != key:
                rename_map[key] = new_id
                new_key_for_cache = new_id
                print(f"{label} -> {new_id}")
            else:
                print(label)
        elif r["outcome"] == "stripped":
            print(f"STRIPPED ({', '.join(r['stripped_fields'])})")
        elif r["outcome"] == "no_match":
            print(f"NO MATCH ({r['reason']})")
        elif r["outcome"] == "skip_misc":
            print("SKIP (misc+url)")
        else:
            print("OK")

        # Update cache
        final_entry = bib_db.entries[i]
        new_cache[new_key_for_cache] = {
            "last_checked": now_iso(),
            "content_hash": entry_hash(final_entry),
            "outcome": r["outcome"],
            "arxiv_id": arxiv_id_from_entry(final_entry) or "",
        }
        if new_key_for_cache != key:
            new_cache.pop(key, None)

    print(f"\nProcessed {processed} of {total} (cached={total - processed})")

    # Dedupe: if multiple entries share an ID (typical when two source keys
    # got renamed to the same DBLP record), keep the first and report the rest.
    seen = {}
    duplicates = []
    deduped = []
    for e in bib_db.entries:
        if e["ID"] in seen:
            duplicates.append(e["ID"])
            continue
        seen[e["ID"]] = True
        deduped.append(e)
    if duplicates:
        bib_db.entries = deduped
        print(f"\nDeduplicated {len(duplicates)} entries collapsed by rename:")
        for k in duplicates:
            print(f"  - {k}")

    # Step 5: rewrite cite sites
    tex_counts = {}
    if rename_map and not args.no_tex_update:
        print(f"\n--- Rewriting cite sites in .tex files ({len(rename_map)} renamed keys) ---")
        tex_counts = rewrite_tex_citations(rename_map, dry_run=args.dry_run)
        for path, n in sorted(tex_counts.items()):
            print(f"  {n:>4}  {path}")
        if not tex_counts:
            print("  (no cite sites rewritten — keys may not be cited)")

    # Write bib
    if not args.dry_run:
        bib_db.entries.sort(key=lambda e: e["ID"].lower())
        writer = BibTexWriter()
        writer.indent = "  "
        writer.comma_first = False
        writer.order_entries_by = ("ID",)
        with open(BIB_FILE, "w", encoding="utf-8") as f:
            f.write(writer.write(bib_db))
        print(f"\nWrote {len(bib_db.entries)} entries to {BIB_FILE.name}")
        save_cache(new_cache)
        print(f"Cache: {CACHE_FILE.relative_to(PROJECT)}")
    else:
        print("\n[dry-run] no files written")

    write_report(removed, results, rename_map, tex_counts)


if __name__ == "__main__":
    main()
